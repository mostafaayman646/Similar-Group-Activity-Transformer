import torch
from torch.utils.data import Dataset
import os
import gc

class FIFASequenceDataset(Dataset):
    def __init__(self, data_dir, match_files, target_frames=100):
        self.target_frames = target_frames
        self.all_features = []
        self.all_labels = []
        self.all_seq_ids = []

        print("Loading matches into memory.")
        file_paths = [os.path.join(data_dir, f) for f in match_files]

        for path in file_paths:
            if not os.path.exists(path):
                print(f"Warning: {path} not found. Skipping.")
                continue

            # Load the dictionary generated by the new dataGenerator.py
            match_data = torch.load(path, weights_only=False)
            
            self.all_features.append(match_data['features'])
            self.all_labels.extend(match_data['labels'])
            self.all_seq_ids.extend(match_data['sequence_ids'])

        if self.all_features:
            self.all_features = torch.cat(self.all_features, dim=0)
        else:
            self.all_features = torch.empty((0, self.target_frames, 23, 7))

        gc.collect()
        print(f"Loaded {len(self.all_features)} total plays across {len(file_paths)} matches!")

    def __len__(self):
        return len(self.all_features)

    def __getitem__(self, idx):
        # Base features from dataGenerator shape: [Frames, 23, 7]
        features = self.all_features[idx].clone()
        
        # Guardrail: Ensure sequence is exactly target_frames (100)
        current_frames = features.shape[0]
        if current_frames >= self.target_frames:
            features = features[-self.target_frames:, :, :]
        else:
            pad = torch.zeros((self.target_frames - current_frames, 23, 7), dtype=features.dtype)
            features = torch.cat([pad, features], dim=0)

        # Build 2 additional features (is_home, is_away) to achieve the 23x9 target shape.
        # Index conventions established in dataGenerator: 0 = Ball, 1:12 = Home, 12:23 = Away
        is_home = torch.zeros((self.target_frames, 23, 1), dtype=features.dtype)
        is_home[:, 1:12, 0] = 1.0
        
        is_away = torch.zeros((self.target_frames, 23, 1), dtype=features.dtype)
        is_away[:, 12:23, 0] = 1.0
        
        # Concatenate features -> final shape: [100, 23, 9]
        final_features = torch.cat([features, is_home, is_away], dim=-1)

        return {
            'features': final_features,
            'supcon_label': self.all_labels[idx],
            'sequence_id': self.all_seq_ids[idx]
        }